public class PrintSentence {
    public static void main(String[] args) {
        // Define a string with special characters using escape sequences
        String sentence = "Here is a sentence with special characters:\n"
                + "1. A tabbed item:\tTabbed item\n"
                + "2. A quoted phrase: \"This is quoted text\"\n"
                + "3. A new line follows this line.\n";

        // Print the sentence
        System.out.println(sentence);
    }
}


