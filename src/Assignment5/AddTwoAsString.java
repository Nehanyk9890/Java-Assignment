public class AddTwoAsString {
    public static void main(String[] args) {
        // Define two numbers as strings
        String number1 = "77648";
        String number2 = "78108";

        // Concatenate the strings
        String concatenatedResult = number1 + number2;

        // Display the result
        System.out.println("The concatenation of " + number1 + " and " + number2 + " is " + concatenatedResult);
    }
}
